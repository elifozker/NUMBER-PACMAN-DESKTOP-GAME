package project2;




public class randomN {
	

		private String number;
		private int x;
		private int y;

		public randomN(int x,int y,String number){

		this.number=number;
		this.x=x;
		this.y=y;
		}
		

		    public int getY() {
		        return y;
		    }

		    public void setY(int y) {
		        this.y = y;
		    }

		    public int getX() {
		        return x;
		    }

		    public void setX(int x) {
		        this.x = x;
		    }

		    public String getNumber() {
		        return number;
		    }

		    public void setNumber(String number) {
		        this.number = number;
		    }
	
}

